#include <stdio.h>
#include <limits.h>

int main()
{
    printf("INT_MIN=%d\n", INT_MIN);
    printf("INT_MAX=%d\n\n", INT_MAX);

    printf("LONG_MIN=%ld\n", LONG_MIN); // long 不一定比int大，要用就用long long
    printf("LONG_MAX=%ld\n\n", LONG_MAX);

    printf("LONG_LONG_MIN=%lld\n", LONG_LONG_MIN);
    printf("LONG_LONG_MAX=%lld\n\n", LONG_LONG_MAX);

    return 0;
}
